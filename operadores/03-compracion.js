//Operadores relacionales
let a = 10;
console.log("Operadores de relacionales");
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//Operadores de igualdad
console.log("Operadores de igualdad");
console.log(a == 10);
console.log(a != 10);
console.log(a == "10"); //Solo valores iguales, no los tipos
console.log(a === "10"); //Validar el valor y el tipo de dato
console.log(a !== "10"); //Validar si son distinto tipo de dato o de valor
