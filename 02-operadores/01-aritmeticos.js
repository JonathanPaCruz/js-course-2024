//Declaración
let a = 5;
let b = 7;

//Expresión
console.log(a + b, "adición"); //adición
console.log(a - b, "resta"); //resta
console.log(a * b, "multiplicación"); //multiplicación
console.log(a / b, "división"); //división
console.log(a % b, "modulo"); //modulo
console.log(a ** b, "potencia"); //potencia

//incremento
console.log(++a); //primero incrementa el valor en 1 [5->6] y lo muestra en consola
console.log(a++); //primero muestra en consola y luego aumenta el valor en 1 [5->6]
console.log(a); //valor real de a terminado
//decremento
console.log(--a);
console.log(a--);
console.log(a);
